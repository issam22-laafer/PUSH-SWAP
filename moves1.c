#include "push_swap.h"

int count_nodes(t_data **stack_a)
{
    t_data  *tmp;
    int     i;

    tmp = *stack_a;
    i = 0;
    while (tmp)
    {
        i++;
        tmp = tmp->next;
    }
    return (i);
}
